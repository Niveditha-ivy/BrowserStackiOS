	int domainID = 0;
	
	PGMobSocket* socket = [self getSocketForSocketID:socketID];
	if( socket )
		domainID = [socket domainID];
	
	return domainID;
}

-(int) getSocketIdForDomain:(int) domainID
{
	int socketID = -1;
	
	PGMobSocket* socket = nil;
	for (socket in mSockets) {
		if([socket domainID] == domainID)
		{
			socketID = [socket socketID];
			break;
		}
	}
	
	return socketID;
	
}

-(NSString*) getIPForSocketId:(int) socketID
{
	PGMobSocket* socket = nil;
	for (socket in mSockets) {
		if([socket socketID] == socketID)
		{
			return [socket serverIP];
		}
	}
	
	return @"";
	
	
}

-(int) getDomainIDForPeer:(int) peerID {
	
	int domainID = DOMAIN_REAL;
	
	PGMobSocket* socket = (PGMobSocket*) [mPeerIDToSocketMap objectForKey:[NSNumber numberWithInt:peerID] ];
	if (socket) {
		domainID = [socket domainID];
	}
	
	return domainID;

}

-(void) removeSocketIfUnUsed:(int) socketID

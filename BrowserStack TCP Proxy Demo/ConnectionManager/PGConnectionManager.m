-(void)loadSSLCertificates
{
#if TARGET_MAC
	CSSM_DATA certData;
#endif
	SecCertificateRef certificateRef = nil;
	
	int certificateLengthList[] = {roo_CertLen, production_CertLen, local_CertLen, bwin_party_CA_cert_len, bwin_party_Test_CA_cert_len, premium_root_certificate_len, premium_production_certificate_len, premium_local_certificate_len};
	unsigned char *certificateData[] = {root_certificate, production_certificate, local_certificate, bwin_party_CA_cert, bwin_party_Test_CA_cert, premium_root_certificate, premium_production_certificate, premium_local_certificate};
	
	mCertificatesList = [NSMutableArray new];
	
	for(int index = 0; index < 8; ++index)
	{
		//creating  certificate
#if TARGET_MAC
		certData.Length = certificateLengthList[index];
		certData.Data = certificateData[index];
        OSStatus status = SecCertificateCreateFromData(&certData, CSSM_CERT_UNKNOWN, CSSM_CERT_ENCODING_UNKNOWN, &certificateRef);
        
        if(status != 0 || !certificateRef)
        {
            CFStringRef errorString = SecCopyErrorMessageString(status, NULL);
            NSString* errorStr = [NSString stringWithFormat:@"PGSocket::loadSSLCertificates SecCertificateCreateFromData   error code:%d errorString:%@",(int)status, errorString];
            PGERROR(errorStr);
            CFRelease(errorString);
            return;
        }
        
        [mCertificatesList addObject:(id)certificateRef];


#else
        CFDataRef certData = CFDataCreate(NULL, certificateData[index], certificateLengthList[index]);
        if (certData != NULL)
        {
            certificateRef = SecCertificateCreateWithData(NULL,certData);
            CFStringRef errorString = SecCertificateCopySubjectSummary(certificateRef);
            NSString* errorStr = [NSString stringWithFormat:@"PGSocket::loadSSLCertificates loaded successfully SecCertificateCreateWithData  errorString:%@", errorString];
            CFRelease(errorString);
            
            if( certificateRef == NULL)
            {
                CFStringRef errorString = SecCertificateCopySubjectSummary(certificateRef);
                NSString* errorStr = [NSString stringWithFormat:@"PGSocket::loadSSLCertificates SecCertificateCreateWithData  errorString:%@", errorString];
                NSLog(errorStr);
                CFRelease(errorString);
                
                CFRelease(certificateRef);
                CFRelease(certData);
                return;
            }
            
            [mCertificatesList addObject:(__bridge id)certificateRef];
            CFRelease(certificateRef);
            CFRelease(certData);
        }
        
#endif
	}
}


